op = '+'
num1 = 10
num2 = 5
if op == '+':
    print(f"{num1} + {num2} = {num1 + num2}")
elif op == '-':
    print(f"{num1} - {num2} = {num1 - num2}")
elif op == '*':
    print(f"{num1} * {num2} = {num1 * num2}")
elif op == '/':
    print(f"{num1} / {num2} = {num1 / num2}")
else:
    print("Invalid operator.")